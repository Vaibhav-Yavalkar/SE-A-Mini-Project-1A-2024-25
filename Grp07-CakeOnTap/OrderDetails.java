    private void jbtndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtndeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel) jtablecart.getModel();
        int SelectedRows = jtablecart.getSelectedRow();
        
        try {
            int pid = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            deleteItem = JOptionPane.showConfirmDialog(null,"Confirm If you want to delete the cake??","warning",JOptionPane.YES_NO_OPTION);
            if (deleteItem==JOptionPane.YES_OPTION)
            {
                Class.forName("com.mysql.jdbc.Driver");
                Con = DriverManager.getConnection(dataConn, username, password);
                pst = Con.prepareStatement("delete from mycart where pid=?");
                pst.setInt(1,pid);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Cake Deleted from Cart!!");
                upDateDB();
                
                       
                
            }
        }catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(ManageCakes.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (SQLException ex){
            System.err.println(ex);
        }
    }//GEN-LAST:event_jbtndeleteActionPerformed

